                                    <h6 class="h6">1. INTRODUCTION</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            APT Securities and Funds Limited collects, holds, processes, and shares
                                            personal data, a valuable asset that needs to be suitably protected
                                        </li>
                                        <li>Every care is taken to protect personal data from incidents (either
                                            accidentally or deliberately) to avoid a data protection breach that could
                                            compromise security.
                                        </li>
                                        <li>
                                            Compromise of information, confidentiality, integrity, or availability may
                                            result in harm to individual(s), reputational damage, detrimental effect on
                                            service provision, legislative non-compliance, and/or financial costs.
                                        </li>
                                    </ul>
                                    </p>
                                    <p>
                                        The nominee service facilitates the management of a client’s holding under a
                                        nominee name. This transaction must however
                                        conform to the ethics of corporate governance and transparency. A periodic
                                        report on the performance of the account will
                                        be communicated to the client.
                                    </p>

                                    <h6 class="h6">2. PURPOSE AND SCOPE</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            APT Securities and Funds Limited is obliged under Data Protection
                                            legislation to have in place an institutional framework designed to ensure
                                            the security of all personal data during its life cycle, including clear
                                            lines of responsibility.
                                        </li>
                                        <li>This policy sets out the procedure to be followed to ensure a consistent and
                                            effective approach is in place for managing data breach and information
                                            security incidents across APT Securities and Funds Limited.
                                        </li>
                                        <li>
                                            This policy relates to all personal and special categories (sensitive) data
                                            held by APT Securities and Funds Limited regardless of format.
                                        </li>
                                        <li>
                                            This policy applies to all employee of APT Securities and Funds Limited.
                                            This includes casual, out-source or agency staff and contractors,
                                            consultants, suppliers and data processors working for, or on behalf of APT
                                            Securities and Funds Limited.
                                        </li>
                                        <li>
                                            The objective of this policy is to contain any breaches, to minimise the
                                            risk associated with the breach and consider what action is necessary to
                                            secure personal data and prevent further breaches.
                                        </li>
                                    </ul>
                                    </p>
                                    <p>
                                        The nominee service facilitates the management of a client’s holding under a
                                        nominee name. This transaction must however
                                        conform to the ethics of corporate governance and transparency. A periodic
                                        report on the performance of the account will
                                        be communicated to the client.
                                    </p>
                                    <h6 class="h6">3. DEFINITIONS / TYPES OF BREACH</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            For the purpose of this policy, data security breaches include both
                                            confirmed and suspected incidents.
                                        </li>
                                        <li>
                                            An incident in the context of this policy is an event or action which may
                                            compromise the confidentiality, integrity or availability of systems or
                                            data, either accidentally or deliberately, and has caused or has the
                                            potential to cause damage to the APT Securities and Funds Limited
                                            information assets and / or reputation.
                                        </li>
                                        <li>
                                            An incident includes but is not restricted to, the following:
                                            <ol>
                                                <li>loss or theft of confidential or sensitive data or equipment on
                                                    which such data is stored (e.g. loss of laptop, USB stick, iPad /
                                                    tablet device, or paper record);
                                                </li>
                                                <li>equipment theft or failure;</li>
                                                <li>unauthorised use of, access to or modification of data or
                                                    information systems;
                                                </li>
                                                <li>attempts (failed or successful) to gain unauthorised access to
                                                    information or IT system(s);
                                                </li>
                                                <li>unauthorised disclosure of sensitive / confidential data;</li>
                                                <li>website defacement;</li>
                                                <li>hacking attack;</li>
                                                <li>unforeseen circumstances such as a fire or flood;</li>
                                                <li>human error;</li>
                                                <li>blagging’ offences where information is obtained by deceiving the
                                                    organisation who holds it
                                                </li>
                                            </ol>
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">4. REPORTING AN INCIDENT</h6>
                                    <p>
                                        Any individual who accesses, uses or manages APT Securities and Funds Limited
                                        information is responsible for reporting data breach and information security
                                        incidents immediately to the Data Protection Officer via dpo@aptsecurities.com
                                    <ul>
                                        <li>
                                            If the breach occurs or is discovered outside normal working hours, it must
                                            be reported as soon as is practicable.
                                        </li>
                                        <li>
                                            The report must include full and accurate details of the incident, when the
                                            breach occurred (dates and times), who is reporting it, if the data relates
                                            to people, the nature of the information, and how many individuals are
                                            involved. An Incident Report Form should be completed as part of the
                                            reporting process (refer to [The data Breach report form]).
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">5. CONTAINMENT AND RECOVERY</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            The Data Protection Officer (DPO) will first determine if the breach is
                                            still occurring. If so, the appropriate steps will be taken immediately to
                                            minimise the effect of the breach..
                                        </li>
                                        <li>
                                            An initial assessment will be made by the DPO in liaison with relevant
                                            officer(s) to establish the severity of the breach and who will take the
                                            lead investigating the breach, as the Lead Investigation Officer (this will
                                            depend on the nature of the breach; in some cases, it could be the DPO).
                                        </li>
                                        <li>
                                            The Data Protection Officer will establish whether there is anything that
                                            can be done to recover any losses and limit the damage the breach could
                                            cause.
                                        </li>
                                        <li>
                                            The Data Protection Officer will establish who may need to be notified as
                                            part of the initial containment and will inform the police, where
                                            appropriate.
                                        </li>
                                        <li>
                                            The Data Protection Officer, in liaison with the relevant officer(s) will
                                            determine the suitable course of action to be taken to ensure a resolution
                                            to the incident.
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">6. INVESTIGATION AND RISK ASSESSMENT</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            An investigation will be undertaken by the DPO with the Head of Information
                                            Technology / Head Internal Control department immediately and wherever
                                            possible, within 24 hours of the breach being discovered / reported.
                                        </li>
                                        <li>
                                            The DPO with the Head of Information Technology / Head of Internal Control
                                            department will investigate the breach and assess the risks associated with
                                            it, for example, the potential adverse consequences for individuals, how
                                            serious or substantial those are and how likely they are to occur.
                                        </li>
                                        <li>The investigation will need to take into account the following:</li>
                                        <li>

                                            <ol>
                                                <li>
                                                    the type of data involved;
                                                </li>
                                                <li>its sensitivity;</li>
                                                <li>what has happened to the data (e.g. has it been lost or stolen);
                                                </li>
                                                <li>whether the data could be put to any illegal or inappropriate use;
                                                </li>
                                                <li>data subject(s) affected by the breach, number of individuals
                                                    involved and the potential effects on those data subject(s);</li>
                                                <li>whether there are wider consequences to the breach.</li>
                                            </ol>
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">7. NOTIFICATION</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            The DPO with the Head of Information Technology / Head of Internal Control
                                            department, in consultation with relevant colleagues will establish whether
                                            the Authority will need to be notified of the breach, and if so, notify them
                                            within 72 hours of becoming aware of the breach, where feasible.
                                        </li>
                                        <li>
                                            Every incident will be assessed on a case-by-case basis; however, the
                                            following will need to be considered:
                                        </li>
                                        <ol>
                                            <li>
                                                whether the breach is likely to result in a high risk of adversely
                                                affecting individuals’ rights and freedoms under Data Protection
                                                legislation;
                                            </li>
                                            <li>whether notification would assist the individual(s) affected (e.g. could
                                                they act on the information to mitigate risks?);</li>
                                            <li>what has happened to the data (e.g. has it been lost or stolen);
                                            </li>
                                            <li>whether notification would help prevent the unauthorised or unlawful use
                                                of personal data;
                                            </li>
                                            <li>whether there are any legal / contractual notification requirements;
                                            </li>
                                            <li>the dangers of over notifying. Not every incident warrants notification
                                                and over notification may cause disproportionate enquiries and work.
                                            </li>
                                        </ol>
                                        </li>
                                        <li>
                                            Individuals whose personal data has been affected by the incident, and where
                                            it has been considered likely to result in a high risk of adversely
                                            affecting that individual’s rights and freedoms, will be informed without
                                            undue delay. Notification will include a description of how and when the
                                            breach occurred and the data involved. Specific and clear advice will be
                                            given on what they can do to protect themselves, and include what action has
                                            already been taken to mitigate the risks. Individuals will also be provided
                                            with a way in which they can contact APT Securities and Funds Limited for
                                            further information or to ask questions on what has occurred APT Securities
                                            and Funds Limited for further information or to ask questions on what has
                                            occurred.
                                        </li>
                                        <li>
                                            The Data Protection Officer must consider notifying third parties such as
                                            the police, insurers, banks or credit card companies. This would be
                                            appropriate where illegal activity is known or is believed to have occurred,
                                            or where there is a risk that illegal activity might occur in the future.
                                        </li>
                                        <li>A record will be kept of any personal data breach, regardless of whether
                                            notification was required.</li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">8. EVALUATION AND RESPONSE</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            Once the initial incident is contained, the DPO will carry out a full review
                                            of the causes of the breach; the effectiveness of the response(s) and
                                            whether any changes to systems, policies and procedures should be
                                            undertaken.
                                        </li>
                                        <li>
                                            Existing controls will be reviewed to determine their adequacy, and whether
                                            any corrective action should be taken to minimise the risk of similar
                                            incidents occurring.
                                        </li>
                                        <li>The review will consider:</li>
                                        <ol>
                                            <li>
                                                where and how personal data is held and where and how it is stored;
                                            </li>
                                            <li>
                                                where the biggest risks lie including identifying potential weak points
                                                within existing security measures;
                                            </li>
                                            <li>whether methods of transmission are secure; sharing minimum amount of
                                                data necessary;
                                            </li>
                                            <li>staff awareness;</li>
                                            <li>implementing a data breach plan and identifying a group of individuals
                                                responsible for reacting to reported breaches of security.
                                            </li>
                                        </ol>
                                    </ul>
                                    </p>
                                    <h6 class="h6">9. POLICY REVIEW</h6>
                                    <ul>
                                        <li>This policy will be updated as necessary to reflect best practice and to
                                            ensure compliance with any changes or amendments to relevant legislation.
                                        </li>
                                        <li>This policy was last reviewed in January 2021.</li>
                                    </ul>
