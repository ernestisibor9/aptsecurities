                                    <h6 class="h6">Objective of the Policy</h6>
                                    <p>
                                    <ol>
                                        <li>
                                            This policy and procedure manual is intended to encourage staff and other
                                            relevant stakeholders to report perceived unethical or illegal conduct of
                                            employees, management, directors and other stakeholders across the company
                                            to appropriate authorities in a confidential manner without any fear of
                                            harassment, intimidation, victimization or reprisal of anyone for raising
                                            concern(s) under this policy. Specific objectives of the policy are: a. To
                                            ensure all employees feel supported in speaking up in confidence and
                                            reporting matters they suspect may involve improper, unethical or
                                            inappropriate conduct within the company; b. To encourage all improper,
                                            unethical or inappropriate behavior to be identified and challenged at all
                                            levels of the company; c. To provide clear procedures for reporting and
                                            handling such concern(s) d. To proactively prevent and deter misconduct
                                            which could impact the financial performance and damage the company`s
                                            reputation; e. To provide assurance that all disclosures will be handled
                                            seriously, treated as confidential and managed without fear of reprisal of
                                            any form; and f. To help promote and develop a culture of openness,
                                            accountability, and integrity.
                                        </li>
                                    </ol>
                                    </p>
                                    <h6 class="h6">Scope of the Policy</h6>
                                    <p>
                                    <ol>
                                        <li>
                                            This policy and procedure manual is designed to enable employees and other
                                            relevant stakeholders to report any perceived act of impropriety which
                                            should not be based on mere speculation, rumors, and gossips but on
                                            knowledge of facts. Reportable misconducts covered under this policy
                                            include:-  All forms of financial malpractices or impropriety such as
                                            fraud, corruption, bribery, theft and concealment;  Failure to comply with
                                            legal obligations, statutes, and regulatory directives;  Actions
                                            detrimental to Health and Safety or the work environment; Any form of
                                            criminal activity;  Improper conduct or unethical behavior that undermines
                                            universal and core ethical values such as integrity, respect, honesty,
                                            accountability and fairness;  Other forms of corporate governance breaches;
                                             Connected transactions not disclosed or reported in line with regulations;
                                             Insider dealing;  Non-disclosure of interests;  Sexual or physical abuse
                                            of staff, customers, prospective staff, service providers and other relevant
                                            stakeholders; and  Attempt to conceal any of the above listed acts. The
                                            above listed reportable misconducts or concerns are not exhaustive. However,
                                            judgment and discretion are required to determine misconduct that should be
                                            reported under this policy. The general guide in identifying reportable
                                            misconduct is to report concerns which are repugnant to the interest of the
                                            company and the general public and appropriate sanctions applied. Finally,
                                            this policy does not cover individual staff grievances and other employee
                                            related matters already covered in the staff hand-books of APT Securities
                                            and Funds Limited.
                                        </li>
                                    </ol>
                                    </p>
                                    <h6 class="h6">Board and Management Commitment to the Policy</h6>
                                    <p>
                                    <ol>
                                        <li>The Board and Management are aware that a robust internal system for
                                            employees and other relevant stakeholders to disclose workplace malpractices
                                            without fear of reprisal shows that employees take their responsibilities
                                            seriously, and also helps to avoid the negative publicity that often
                                            accompanies disclosures to external parties. Hence the Board of Directors
                                            and Management is committed towards promoting a culture of openness,
                                            accountability and integrity, and will not tolerate any harassment,
                                            victimization or discrimination of the whistleblower provided such
                                            disclosure is made in good faith with reasonable belief that what is being
                                            reported is fact.</li>
                                    </ol>
                                    </p>
                                    <h6 class="h6">Policy Statement</h6>
                                    <p>
                                    <ol>
                                        <li>APT Securities and Funds Limited is committed to the highest standards of
                                            openness, probity, accountability and high ethical behavior by helping to
                                            foster and maintain an environment where employees and other stakeholders
                                            can act appropriately, without fear of reprisal. To maintain these
                                            standards, the company encourages employees and relevant stakeholders who
                                            have material concerns about suspected misconduct or any breach or suspected
                                            breach of law or regulation that may adversely impact the group, to come
                                            forward and report them through appropriate channels (in certain cases on a
                                            confidential basis) without fear of retribution or unfair treatment. The
                                            company conducts its business on the principles of fairness, honesty,
                                            openness, decency, integrity and respect. It is the intention of this policy
                                            to encourage employees and other relevant stakeholders to report and
                                            disclose improper or illegal practices or activities. The company is
                                            committed to investigate promptly any reported misconduct and to protect
                                            those who come forward to report such activities. The company further
                                            assures that all reports shall be treated in strict confidence. The
                                            Company`s operating procedures are intended to detect and prevent or deter
                                            improper activities. However, the best systems of controls may not provide
                                            absolute safeguards against irregularities. This policy is intended to
                                            investigate and take appropriate action against any reported misconduct or
                                            concern.</li>
                                    </ol>
                                    </p>
                                    <h6 class="h6">Whistleblowing Procedure</h6>
                                    <p>
                                    <ol>
                                        <li>
                                            The whistleblowing procedure involves steps that should be taken by the
                                            whistleblower in reporting misconduct, and steps required for the
                                            investigation of the reported misconduct. The following procedures shall
                                            guide the whistleblowing process: 7.1 Internal Whistleblowing Procedure
                                            Internal whistleblowing involves staff members across the company raising
                                            concerns about unethical conduct. The following procedure shall be adopted
                                            for the purpose of internal whistleblowing: S/N Steps Action 1 Step One
                                            Raising concern(s) by whistleblower - medium and format. An internal
                                            whistleblower may raise concern through any of the following media (this can
                                            be done either by declaration or in confidence/ anonymously):  Formal
                                            letter to the Managing Director, or the Head Internal Audit APT Securities
                                            and Funds limited  Dedicated phone number/ communicator chat. 08033054105
                                            MD/CEO, 08033146939 Chairman Audit Committee,  Dedicated email address:
                                            aptsecuritieswhistleblowing@aptsecurities.com  agumi@yahoo.com  Via APT
                                            securities website: Where the concern is received by staff other than the
                                            Managing Director or the Head, Internal Audit, the recipient of such
                                            concerns shall be required to;  Immediately pass the concern(s) to the
                                            Head, Internal Audit with copy to the Managing Director, Apt securities and
                                            Funds limited.  If the concerns affect the Head, of Internal Audit, the
                                            Managing Director, Apt securities and Funds shall be notified; and where a
                                            Managing Director/CEO is involved, such concern shall be directed at the
                                            Chairman Board Audit Committee and where it involve chairman board audit
                                            committee, the chairman of the company shall be notified, where it involve
                                            chairman of the company the Securities and exchange commission or NGX shall
                                            be notified. The concern(s) shall be presented in the following format; 
                                            Background of the concerns (with relevant dates)  Reason(s) why the
                                            whistleblower is particularly concerned about the situation. Disciplinary
                                            measures in line with the staff handbook shall be taken against any staff
                                            that receives concerns and fails to escalate. Also, disciplinary measure
                                            shall be taken against an internal whistleblower who acted out of malice. 2
                                            Step Two Investigation of Concerns and update on progress of investigation.
                                            The Head, Internal Audit shall on receipt of the concern(s) acknowledge
                                            receipt of the concern from the whistleblower within 3 working days, and
                                            immediately commence investigation. The purposes of investigation are to: a.
                                            Establish if a wrongdoing has occurred based on the concern(s) raised, and
                                            if so to what extent; and b. To minimize the risk of further wrongdoing,
                                            prevent any further loss of assets, damage to the group`s reputation and if
                                            possible protect all sources of evidence. If preliminary investigation shows
                                            that the concern falls within the whistleblowing reportable concerns, then
                                            further investigation shall be carried out. If otherwise or the concern is
                                            outside the reportable misconduct, then the Head, Internal Audit shall refer
                                            the matter to appropriate quarters for further action. Where necessary the
                                            Head, Internal Audit shall provide update of the progress of investigation
                                            to the whistleblower if the concerns fall within the reportable concerns.
                                            Finally, if the concern raised by the whistleblower is frivolousor
                                            unwarranted, the Head, Internal Audit shall ignore such concern, and where
                                            necessary disciplinary measure in line with Human Resources policy shall
                                            apply to staff involved. 3 Step Three Report of Investigation and action on
                                            report. Upon conclusion of investigation, the Head, Internal Auditshall
                                            submit his/her report to the Human Resources or the appropriate authority
                                            for further action(s). Where necessary the Head, Internal Audit shall
                                            escalate to the Managing Director. However, annual report to keep the
                                            Managing Director abreast of developments in whistleblowing shall be
                                            submitted by Head, Internal Audit. All disciplinary action relating to the
                                            report shall follow the Group`s disciplinary procedure as contained in the
                                            staff hand book. 4 Step Four Non-Satisfaction with result of
                                            investigation/action In the event that the whistleblower is not satisfied
                                            with the extent of investigation and or the action taken based on the
                                            outcome of the investigation, the whistleblower is at liberty to report to
                                            the Chairman of the Board Audit Committee. Any internal whistleblower that
                                            feels victimized can report his/her grievance(s) to the Chairman, Board
                                            Audit Committee. This is without prejudice to the fundamental right of the
                                            internal whistleblower to seek redress in the court of law. 7.2 External
                                            Whistleblowing Procedure External whistleblowers are non-staff of the
                                            company. External whistleblowers can fall into any of these categories:
                                            contractors, service providers, shareholders, depositors, analysts,
                                            consultant, job applicants, and the general public. External whistleblowing
                                            shall follow the following procedure: S/N Steps Required Action 1 Step One
                                            Raising concern(s) by whistleblower: - medium and format. An external
                                            whistleblower may raise concern through anyof the following media (this can
                                            be done either by declaration or in confidence/anonymously):  By a formal
                                            letter to the Managing Director, or Head, Internal Audit.  Dedicated phone
                                            number as contained on the website: www.aptsecurities.com  Dedicated email
                                            address:  Electronically log into: www.aptsecurities.com  Directly to the
                                            Managing Director, Apt securities and funds limited.  Directly to the Head,
                                            Internal Audit, Apt securities and Funds Ltd. Where the concern is received
                                            by staff other than the Managing Director and the Head, Internal Audit, the
                                            recipient of such concerns shall be required to;  Immediately pass the
                                            concern(s) to the Head, Internal Audit with copy to the Managing Director
                                            but ensuring confidentiality of the concern.  If the concerns affect the
                                            Head, Internal Audit, the Managing Director is notified, and where Executive
                                            Director is involved, such concern shall be directed at the chairman Board
                                            Audit Committee. The concern(s) shall be presented in the following format;
                                             Background of the issue (with relevant dates)  Reason(s) why the
                                            whistleblower is particularly concerned about the situation. Disciplinary
                                            measures in line with the staff hand book shall be taken against any staff
                                            that receives concerns from an external whistleblower and fails to pass same
                                            to the appropriate authority. 2 Step Two Investigation of Concerns and
                                            update on progress of investigation. The Head, Internal Audit shall on
                                            receipt of the concern(s) acknowledge receipt from the whistleblower within
                                            3 working days, and immediately commence investigation. The purpose of
                                            investigation is to: a. Establish if a wrongdoing has occurred based on the
                                            concern(s) raised, and if so to what extent; and b. To minimize the risk of
                                            further wrong doing, prevent any further loss of assets, damage to the
                                            group`s reputation and if possible, protect all sources of evidence. If
                                            preliminary investigation shows that the concern falls within the
                                            whistleblowing reportable concerns, then further investigation shall be
                                            carried out. If otherwise, the Head, Internal Audit shall refer the matter
                                            to the appropriate quarters for further action. However, if the concern
                                            raised by the whistleblower is frivolous or unwarranted, disciplinary action
                                            shall be taken Where it is established that a criminal activity has taken
                                            place, the matter may be referred to the Nigerian Police Force, and where
                                            necessary, appropriate legal action taken. Where necessary the Head,
                                            Internal Audit shall provide update of the progress of investigation to the
                                            whistleblower. 3 Step Three Report of Investigation and action on report.
                                            Upon conclusion of investigation, the Head, Internal Audit shall submit
                                            his/her report to the Human Resources or the appropriate authority for
                                            further action(s). Where necessary the Head, Internal Audit shall escalate
                                            to the Managing Director. However, annual report to keep theManaging
                                            Director abreast of developments in whistleblowing shall be submitted by
                                            Head, Internal Audit. If the concern(s) relates to an Executive Director,
                                            the matter shall be referred to the Chairman Board Audit Committee for
                                            further action. If the concern(s) relates to an external party (service
                                            provider), the company shall immediately review the Service Level Agreement
                                            with such service provider, and if necessary terminate the agreement. 4 Step
                                            Four Non-Satisfaction with result of investigation/action. In the event that
                                            the whistleblower is not satisfied with the extent of investigation and or
                                            the action taken based on the outcome of the investigation, the
                                            whistleblower is at liberty to report to the Chairman of the Board Audit
                                            Committee for further action. An external whistleblower shall be at liberty
                                            to report to appropriate regulatory body or seek further redress in the
                                            court of laws If he/she is not satisfied with the action taken to address
                                            the concern(s). 8. Time Limit for Investigation It shall be the policy of
                                            the company to handle investigations promptly and as fairly as possible.
                                            While it might not be possible to set a specified time frame for the
                                            conclusion of investigation, since the diverse nature of potential concerns
                                            may make this impracticable. The Head, Internal Audit shall endeavor to
                                            resolve all concerns within five weeks. Where for any reason, proper
                                            resolution is unable to be achieved within this time frame; the Head,
                                            Internal Audit shall advice the Managing Director accordingly, and report to
                                            the Chairman, Board Audit and Risk Assessment Committee. 9. Protection and
                                            Compensation for Whistleblower It shall be the policy of the company to
                                            protect whistleblowers who disclose concerns, provided the disclosure is
                                            made;  in the reasonable belief that it is intended to show malpractice or
                                            impropriety;  to an appropriate person or authority; and  In good faith
                                            without malice or mischief. While all disclosures resulting from
                                            whistleblowing shall be treated with high level of confidentiality, staff
                                            and other relevant stakeholders are encouraged to disclose their name to
                                            make the report more credible. The company shall take the following into
                                            consideration in considering unanimous disclosure:  seriousness of the
                                            issues being reported;  the significance and credibility of the concern;
                                            and  the possibility of confirming the allegation. Where necessary,
                                            compensation of whistleblowers whether internal or external that have
                                            suffered detriment shall be at the discretion of Management taking into
                                            consideration regulatory guidance on compensation of whistleblower to be
                                            issued from time to time. Any retaliation, including, but not limited to,
                                            any act of discrimination, reprisal, harassment, suspension, dismissal,
                                            demotion, vengeance or any other occupational detriment, direct or indirect,
                                            recommended, threatened or taken against a whistleblower because he/she has
                                            made a disclosure in accordance with this policy will be treated as gross
                                            misconduct and dealt with accordingly. Whistleblowers must ensure that they
                                            do not make disclosure outside of the prescribed channels (e.g. media-print
                                            or electronic), or their disclosures may not be protected
                                        </li>
                                    </ol>
