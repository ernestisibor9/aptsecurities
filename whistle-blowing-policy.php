                                    <h6 class="h6">Introduction </h6>
                                    <p>
                                    <ol>
                                        <li>
                                            APT Security and Funds Limited in ensuring a high ethical standard in all
                                            its business activities has established a code of ethics which set out the
                                            standard of conduct expected in the management of its businesses across the
                                            company. All stakeholders are expected to comply with these standards in the
                                            discharge of their duties. In furtherance of this, APT securities and Funds
                                            Limited Whistleblowing Policy and Procedure provides achannel for the
                                            company`s employees and other relevant stakeholders to raise concerns about
                                            workplace malpractices, in a confidential manner; for the company to
                                            investigate alleged malpractices and take steps to deal with such in a
                                            manner consistent with the company’s policies and procedures and relevant
                                            regulations. Whistleblowing for the purpose of this policy is the act of
                                            reporting perceived unethical conduct of employees, management, directors,
                                            and other stakeholders by an employee orother persons to appropriate
                                            authorities. This policy and procedure manual outline the company`s Policy
                                            on whistleblowing and the procedure for investigating and dealing with all
                                            reported cases of illegal and unethical conduct and any other misconduct
                                            across the company. This policy is in compliance with the requirements of
                                            various regulatory authorities with oversight on the activities of the
                                            company on whistleblowing, particularly section 32 page 428 of the
                                            Securities and Exchange Commission rule book 2013 “whistleblowing policy”.
                                        </li>
                                    </ol>
