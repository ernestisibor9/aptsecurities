                                <div>
                                    <h6 class="h6">1. ABOUT THIS POLICY: SCOPE, PURPOSE AND USERS</h6>
                                    <ul>
                                        <li>This procedure sets out the key features regarding handling or responding to
                                            requests for access to personal data made by data subjects, their
                                            representatives or other interested parties. This procedure will enable APT
                                            Securities and Funds Limited (further: “Company”) to comply with legal
                                            obligations, provide better customer care, improve transparency, enable
                                            individuals to verify that information held about them is accurate, and
                                            increase the level of trust by being open with individuals about the
                                            information that is held about them.
                                        </li>
                                        <li>
                                            This procedure applies broadly across all entities or subsidiaries owned or
                                            operated by the Company but does not affect any state or local laws or
                                            regulations which may otherwise be applicable.
                                        </li>
                                        <li>
                                            This procedure applies to employees that handle data subject access requests
                                            such as the Data Protection Officer.
                                        </li>
                                    </ul>
                                    <h6 class="h6">2. DATA SUBJECT ACCESS REQUEST (DSAR)</h6>
                                    <ul>
                                        <li>A Data Subject Access Request (DSAR) is any request made by an individual or
                                            an individual’s legal representative for information held by the Company
                                            about that individual. The Data Subject Access Request provides the right
                                            for data subjects to see or view their own personal data as well as to
                                            request copies of the data.
                                        </li>
                                        <li>
                                            A Data Subject Access Request must be made in writing. In general, verbal
                                            requests for information held about an individual are not valid DSARs.
                                        </li>
                                        <li>
                                            A Data Subject Access Request can be made via any of the following methods:
                                            email or post. DSARs made on-line must be treated like any other Data
                                            Subject Access Requests when they are received, though the Company will not
                                            provide personal information via social media channels.
                                        </li>
                                    </ul>

                                    <h6 class="h6">3. THE RIGHTS OF A DATA SUBJECT</h6>
                                    <p>The rights to data subject access include the following:</p>
                                    <ul>
                                        <li>
                                            Know whether a data controller holds any personal data about them.
                                        </li>
                                        <li>
                                            Receive a description of the data held about them and, if permissible and
                                            practical, a copy of the data.
                                        </li>
                                        <li>
                                            Be informed of the purpose(s) for which that data is being processed, and
                                            from where it was received.
                                        </li>
                                        <li>
                                            Be informed whether the information is being disclosed to anyone apart from
                                            the original recipient of the data; and if so, the identity of those
                                            recipients.
                                        </li>
                                        <li>
                                            The right of data portability. Data subjects can ask that their personal
                                            data be transferred to them or a third party in machine readable format
                                            (Word, PDF, etc.). However, such requests can only be fulfilled if the data
                                            in question is: 1) provided by the data subject to the Company, 2) is
                                            processed automatically and 3) is processed based on consent or fulfilment
                                            of a contract.
                                        </li>
                                        <li>If the data is being used to make automated decisions about the data
                                            subject, to be told what logic the system uses to make those decisions and
                                            to be able to request human intervention.
                                        </li>
                                        <li>
                                            The Company must provide a response to data subjects requesting access to
                                            their data within 30 calendar days of receiving the Data Subject Access
                                            Request unless local legislation dictates otherwise.
                                        </li>
                                    </ul>


                                    <h6 class="h6">4. REQUIREMENTS FOR A VALID DSAR</h6>
                                    <ul>
                                        <li>
                                            In order to be able to respond to the Data Subject Access Requests in a
                                            timely manner, the data subject should:
                                            <ul style="list-style-type: square;">
                                                <li>Submit his/her request using a Data Subject Access Request Form</li>
                                                <li>Provide the Company with sufficient information to validate his/her
                                                    identity (to ensure that the person requesting the information is
                                                    the data subject or his/her authorized person)
                                                </li>
                                            </ul>
                                        </li>
                                        <li>
                                            Subject to the exemptions referred to in this document, the Company will
                                            provide information to data subjects whose requests are in writing (or by
                                            some other method explicitly permitted by the local law), and are received
                                            from an individual whose identity can be validated by Company.
                                        </li>
                                        <li>
                                            However, Company will not provide data where the resources required to
                                            identify and retrieve it would be excessively difficult or time-consuming.
                                            Requests are more likely to be successful where they are specific and
                                            targeted at particular information.
                                        </li>
                                        <li>
                                            Factors that can assist in narrowing the scope of a search include
                                            identifying the likely holder of the information (e.g. by making reference
                                            to a specific department), the time period in which the information was
                                            generated or processed (the narrower the time frame, the more likely a
                                            request is to succeed) and being specific about the nature of the data
                                            sought (e.g. a copy of a particular form or email records from within a
                                            particular department).
                                        </li>
                                    </ul>

                                    <h6 class="h6">5. DSAR PROCESS</h6>
                                    <ul>
                                        <li>
                                            <strong>Request </strong> Upon receipt of a DSAR, the Data Protection Team
                                            will log and
                                            acknowledge the request. The requestor may be asked to complete a Data
                                            Subject Access Request Form to better enable the Company to locate the
                                            relevant information.
                                        </li>
                                        <li>
                                            <strong> Identify verification</strong> The Data Protection Team needs to
                                            check the identity
                                            of anyone making a DSAR to ensure information is only given to the person
                                            who is entitled to it. If the identity of a DSAR requestor has not already
                                            been provided, the person receiving the request will ask the requestor to
                                            provide two forms of identification, one of which must be a photo identity
                                            and the other confirmation of address. If the requestor is not the data
                                            subject, written confirmation that the requestor is authorized to act on
                                            behalf of the data subject is required.
                                        </li>
                                        <li>
                                            <strong>Information for DSAR</strong> Upon receipt of the required
                                            documents, the person
                                            receiving the request will provide the Data Protection Team with all
                                            relevant information in support of the DSAR. Where the Data Protection Team
                                            is reasonably satisfied with the information presented by the person who
                                            received the request, the Data Protection Officer will notify the requestor
                                            that his/her DSAR will be responded to within 30 calendar days. The 30-day
                                            period begins from the date that the required documents are received. The
                                            requestor will be informed by the Data Protection Team in writing if there
                                            will be any deviation from the 30-day time-frame due to other intervening
                                            events.
                                        </li>
                                        <li>
                                            <strong> Review of Information</strong> The Data Protection Team composed of
                                            cross department
                                            representative will collate the relevant and required information as
                                            requested in the DSAR. The Data Protection Team must ensure that the
                                            information is reviewed/received by the imposed deadline to ensure the
                                            30-calendar day time-frame is not breached. The Data Protection Officer will
                                            ask the relevant department to complete a “Data Subject Disclosure Form” to
                                            document compliance with the 30-day requirement.
                                        </li>
                                        <li>
                                            <strong> Response to access requests</strong> The Data Protection Team will
                                            provide the
                                            finalized response together with the information retrieved and/or a
                                            statement that the Company does not hold the information requested, or that
                                            an exemption applies. The Data Protection Team will ensure that a written
                                            response will be sent back to the requestor. This will be via email, unless
                                            the requestor has specified another method by which they wish to receive the
                                            response (e.g. post). The Company will only provide information via channels
                                            that are secure. When hard copies of information are posted, they will be
                                            sealed securely and sent by recorded delivery.
                                        </li>
                                        <li><strong>Archiving</strong> After the response has been sent to the
                                            requestor, the DSAR will
                                            be considered closed and archived by the Data Protection Team.</li>
                                    </ul>

                                    <h6 class="h6">6. EXEMPTIONS</h6>
                                    <p>
                                    <ul>
                                        <li>
                                            An individual does not have the right to access information recorded about
                                            someone else, unless they are an authorized representative.
                                        </li>
                                        <li>
                                            The Company is not required to respond to requests for information unless it
                                            is provided with sufficient details to enable the location of the
                                            information to be identified, and to satisfy itself as to the identity of
                                            the data subject making the request.
                                        </li>
                                        <li>
                                            In principle, the Company will not normally disclose the following types of
                                            information in response to a Data Subject Access Request:
                                            <ul style="list-style-type: square;">
                                                <li>
                                                    Information about other people – A Data Subject Access Request may
                                                    cover information which relates to an individual or individuals
                                                    other than the data subject. Access to such data will not be
                                                    granted, unless the individuals involved consent to the disclosure
                                                    of their data.
                                                </li>
                                                <li>
                                                    Repeat requests – Where a similar or identical request in relation
                                                    to the same data subject has previously been complied with within a
                                                    reasonable time period, and where there is no significant change in
                                                    personal data held in relation to that data subject, any further
                                                    request made within a six-month period of the original request will
                                                    be considered a repeat request, and the Company will not normally
                                                    provide a further copy of the same data
                                                </li>
                                                <li>
                                                    Publicly available information – The Company is not required to
                                                    provide copies of documents which are already in the public domain
                                                </li>
                                                <li>
                                                    Opinions given in confidence or protected by copyright law – The
                                                    Company does not have to disclose personal data held in relation to
                                                    a data subject that is in the form of an opinion given in confidence
                                                    or protected by copyright law.
                                                </li>
                                            </ul>
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">7. DSAR REFUSALS</h6>
                                    <p>There are situations where individuals do not have a right to see information
                                        relating to them. For instance:
                                    </p>
                                    <ul>
                                        <li>
                                            If the information is kept only for the purpose of statistics or research,
                                            and where the results of the statistical work or research are not made
                                            available in a form that identifies any of the individuals involved.
                                        </li>
                                        <li>
                                            Requests made for other, non-data protection purposes can be rejected.
                                        </li>
                                        <li>
                                            If the responsible person refuses a Data Subject Access Request on behalf of
                                            the Company, the reasons for the rejection must be clearly set out in
                                            writing. Any individual dissatisfied with the outcome of his/her Data
                                            Subject Access Request is entitled to make a request to the Data Protection
                                            Officer to review the outcome.
                                        </li>
                                    </ul>
                                    <h6 class="h6">8. RESPONSIBILITIES</h6>
                                    <ul>
                                        <li>
                                            The overall responsibility for ensuring compliance with a DSAR rests with
                                            the Data Protection Officer.
                                        </li>
                                        <li>
                                            If the Company acts as a data controller towards the data subject making the
                                            request then the DSAR will be addressed based on the provisions of this
                                            procedure.
                                        </li>
                                        <li>
                                            If the Company acts as a data processor the Data Protection Officer will
                                            forward the request to the appropriate data controller on whose behalf the
                                            Company processes personal data of the data subject making the request.
                                        </li>

                                    </ul>
