                                        <p class="mb-0" style="text-align: justify;">
                                            The rights to data subject access include the following:
                                        <ul>
                                            <li>
                                                Know whether a data controller holds any personal data about them.
                                            </li>
                                            <li>
                                                Receive a description of the data held about them and, if permissible
                                                and practical, a copy of the data.
                                            </li>
                                            <li>
                                                Be informed of the purpose(s) for which that data is being processed,
                                                and from where it was received.
                                            </li>
                                            <li>
                                                Be informed whether the information is being disclosed to anyone apart
                                                from the original recipient of the data; and if
                                                so, the identity of those recipients.
                                            </li>
                                            <li>
                                                The right of data portability. Data subjects can ask that their personal
                                                data be transferred to them or a third party in
                                                machine readable format (Word, PDF, etc.). However, such requests can
                                                only be fulfilled if the data in question is: 1)
                                                provided by the data subject to the Company, 2) is processed
                                                automatically and 3) is processed based on consent or
                                                fulfilment of a contract.
                                            </li>
                                            <li>
                                                If the data is being used to make automated decisions about the data
                                                subject, to be told what logic the system uses to
                                                make those decisions and to be able to request human intervention.
                                            </li>
                                            <li>
                                                The Company must provide a response to data subjects requesting access
                                                to their data within 30 calendar days of
                                                receiving the Data Subject Access Request unless local legislation
                                                dictates otherwise.
                                            </li>
                                        </ul>
