                                        <h5 style="font-weight: 200;">2. DATA SUBJECT ACCESS REQUEST (DSAR)</h5>
                                        <p class="mb-0" style="text-align: justify;">
                                            A Data Subject Access Request (DSAR) is any request made by an individual or
                                            an individual’s legal representative for
                                            information held by the Company about that individual. The Data Subject
                                            Access Request provides the right for data
                                            subjects to see or view their own personal data as well as to request copies
                                            of the data.
                                        </p>
                                        <p>
                                            A Data Subject Access Request must be made in writing. In general, verbal
                                            requests for information held about an
                                            individual are not valid DSARs.
                                        </p>
                                        <p>
                                            A Data Subject Access Request can be made via any of the following methods:
                                            email or post. DSARs made on-line must be
                                            treated like any other Data Subject Access Requests when they are received,
                                            though the Company will not provide personal
                                            information via social media channels.
                                        </p>
