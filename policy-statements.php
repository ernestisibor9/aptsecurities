                                        <ul>
                                            <li>
                                                Request Upon receipt of a DSAR, the Data Protection Team will log and
                                                acknowledge the request. The requestor may be
                                                asked to complete a Data Subject Access Request Form to better enable
                                                the Company to locate the relevant information.
                                            </li>
                                            <li>
                                                Identify verification The Data Protection Team needs to check the
                                                identity of anyone making a DSAR to ensure information
                                                is only given to the person who is entitled to it. If the identity of a
                                                DSAR requestor has not already been provided,
                                                the person receiving the request will ask the requestor to provide two
                                                forms of identification, one of which must be a
                                                photo identity and the other confirmation of address. If the requestor
                                                is not the data subject, written confirmation
                                                that the requestor is authorized to act on behalf of the data subject is
                                                required.
                                            </li>
                                            <li>
                                                Information for DSAR Upon receipt of the required documents, the person
                                                receiving the request will provide the Data
                                                Protection Team with all relevant information in support of the DSAR.
                                                Where the Data Protection Team is reasonably
                                                satisfied with the information presented by the person who received the
                                                request, the Data Protection Officer will notify
                                                the requestor that his/her DSAR will be responded to within 30 calendar
                                                days. The 30-day period begins from the date
                                                that the required documents are received. The requestor will be informed
                                                by the Data Protection Team in writing if there
                                                will be any deviation from the 30-day time-frame due to other
                                                intervening events.
                                            </li>
                                            <li>
                                                Review of Information The Data Protection Team composed of cross
                                                department representative will collate the relevant and
                                                required information as requested in the DSAR. The Data Protection Team
                                                must ensure that the information is
                                                reviewed/received by the imposed deadline to ensure the 30-calendar day
                                                time-frame is not breached. The Data Protection
                                                Officer will ask the relevant department to complete a “Data Subject
                                                Disclosure Form” to document compliance with the
                                                30-day requirement.
                                            </li>
                                            <li>
                                                Response to access requests The Data Protection Team will provide the
                                                finalized response together with the information
                                                retrieved and/or a statement that the Company does not hold the
                                                information requested, or that an exemption applies. The
                                                Data Protection Team will ensure that a written response will be sent
                                                back to the requestor. This will be via email,
                                                unless the requestor has specified another method by which they wish to
                                                receive the response (e.g. post). The Company
                                                will only provide information via channels that are secure. When hard
                                                copies of information are posted, they will be
                                                sealed securely and sent by recorded delivery.
                                            </li>
                                            <li>
                                                Archiving After the response has been sent to the requestor, the DSAR
                                                will be considered closed and archived by the Data
                                                Protection Team.
                                            </li>
                                        </ul>
                                        </p>
