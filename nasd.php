                                    <h6 class="display-5">FAQ</h6>
                                    <h6 class="h6">WHAT IS NASD PLC?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD Plc is a Securities Exchange registered by the Securities and Exchange
                                        Commission [“SEC”] to operate a formal
                                        Over-The-Counter [“OTC”] market in Nigeria. The acronym NASD was derived from
                                        the company’s origin -the "National
                                        Association of Securities Dealers".
                                    </p>
                                    <h6 class="h6">WHAT SERVICE DOES NASD PLC. PROVIDE?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD Plc. provides trading platforms where all instruments not listed on a
                                        traditional exchange, registered by the
                                        Securities and Exchange Commission, can be traded through licensed stockbroking
                                        houses. It also provides: - a regulated
                                        marketplace for OTC transactions; - an avenue for shareholders to trade shares
                                        of unlisted public companies; - a
                                        centralised source of information and price discovery on unlisted public
                                        companies.
                                    </p>
                                    <h6 class="h6">WHAT IS THE OWNERSHIP STRUCTURE OF NASD?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD is currently owned by fifty-nine (59) capital market operators in Nigeria
                                        and has an authorised Share Capital of
                                        N500 Million. Being a public company however, the shares are freely transferable
                                        to any interested investor and may be
                                        bought on the OTC market when available.
                                    </p>
                                    <h6 class="h6">WHAT IS THE NASD OTC PLATFORM?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        The NASD OTC platform is an electronic inter-dealer quotation system that
                                        displays historical price data, current price
                                        quotes, and market depth information on all long-term securities registered by
                                        the Securities and Exchange Commission
                                        but not listed on any other exchange in Nigeria. The platform provides an avenue
                                        for securities to be traded without the
                                        issuer being listed thereby providing more transparency and ultimately liquidity
                                        to the shareholder and the capital
                                        market in general.
                                    </p>
                                    <h6 class="h6">NASD FACILITATES INTERNET TRADING, WHAT SECURITY IS IN PLACE TO
                                        PROTECT THE INTEGRITY OF THE DEALS?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD platform has encrypted login access, security certificates, clustering and
                                        replication of environment for
                                        continuous market availability.
                                    </p>
                                    <h6 class="h6">WHO REGULATES THE NASD OTC MARKET?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        The NASD Securities Exchange is regulated by the Commission and it is also a
                                        Self-Regulatory Organization [“SRO”]. Who
                                        are the operators in the NASD OTC market?
                                    <ul>
                                        <li>Broker/Dealers registered with NASD introduce securities and make market
                                            (either buy or sell).
                                        </li>
                                        <li>Accredited Issuing Houses are licensed with NASD to provide financial and
                                            Investment advice to Issuers about
                                            participating on the OTC market.*
                                        </li>
                                        <li>Central Securities Clearing System [“CSCS”] is the central depository for
                                            NASD OTC market and ensures completion of
                                            delivery of transactions A list of all participants is available on our
                                            website
                                        </li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">WHAT ARE THE OFFICIAL TRADING HOURS ON THE NASD OTC MARKET?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Trades can be executed on NASD OTC market between the hours of 9.30 a.m. and
                                        3.30 p.m. on weekdays (with the exception
                                        of public holidays)
                                    </p>
                                    <h6 class="h6">WHAT RULES GUIDING TRANSACTION ON NASD OTC MARKET?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        The rules and regulations of the NASD OTC market are designed to protect
                                        investors, regulate the activities of
                                        Participating Institutions and create an orderly environment for efficient
                                        trading of unquoted securities. The full
                                        rules of the market can be obtained from our website at this link
                                    </p>
                                    <h6 class="h6">WHAT ARE THE BENEFITS OF PARTICIPATING IN THE NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                    <ul>
                                        <li>Transparency in trade which enhances investors’ confidence</li>
                                        <li>A centralized platform for price discovery.</li>
                                        <li>A source of long-term funding for companies.</li>
                                        <li>Access to broad range of asset classes.</li>
                                        <li>Access to specialized market data.</li>
                                        <li>Innovation of new and unique investment solutions.</li>
                                        <li>Increased corporate visibility which brings economies and savings.</li>
                                        <li>Improved investor / Shareholder relations.</li>
                                    </ul>
                                    </p>
                                    <h6 class="h6">HOW WILL NASD MONITOR TRADE ACTIVITY ON THE OTC MARKET?</h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD's administrative control facility carries out surveillance on market
                                        activities. Trader activity is actively
                                        monitored both on and off the trading platform.
                                    </p>
                                    <h6 class="h6">ARE THERE PENALTIES FOR DEFAULTING PARTICIPATING INSTITUTIONS?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        The integrity of the market is critical to the continued growth of OTC
                                        market in Nigeria. Defaulters will face
                                        sanctions, fines and/or suspension from trading as may be imposed by the
                                        NASD Disciplinary Committee.
                                    </p>
                                    <h6 class="h6">HOW ARE TRADE DISPUTES SETTLED BY NASD?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        NASD has established a Disciplinary Committee to handle disputes on the market
                                        and have put in place rules and
                                        regulations guiding market activity. Investors can submit complaints or report
                                        any market infraction directly to NASD or
                                        via email to whistleblowing@nasdng.com. The Disciplinary Committee shall
                                        investigate the complaints, give both sides an
                                        opportunity to be heard and make a decision on the issue.
                                    </p>
                                    <h6 class="h6">WHAT IS THE DIFFERENCE BETWEEN TRANSFER AND TRADING?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Transfer and trading refers to the same thing, the two words both mean change of
                                        ownership of a share certificate.
                                    </p>
                                    <h6 class="h6">WHAT SECURITIES ARE TRADABLE ON NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        All unlisted securities of public companies registered with Securities and
                                        Exchange Commission are tradable on NASD OTC
                                        Market.
                                    </p>
                                    <h6 class="h6">WHAT IS A PUBLIC UNLISTED EQUITY?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        A public unlisted stock is a unit of share of a public limited company [“Plc”]
                                        that is not quoted on the board of any
                                        registered exchange in Nigeria. It is often referred to as share.
                                    </p>
                                    <h6 class="h6">WHAT IF THE SECURITY OF A PUBLIC COMPANY IS NOT REGISTERED WITH
                                        SECURITY AND EXCHANGE COMMISSION?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Section 54 (1) of the Investment and Securities Act states that "All securities
                                        of public companies and all Investments
                                        of a collective investment scheme shall be registered with the Commission under
                                        the terms and conditions contained
                                        herein". Accordingly, NASD will not trade the securities of public companies
                                        that are not registered with the
                                        Commission. Such securities contravene investment laws, attract penalties and
                                        may expose the investor to undue risk. The
                                        issuer of such securities should immediately register the securities with the
                                        Commission. Once the Commission has
                                        registered the securities, they can then be traded on the NASD OTC market.
                                    </p>
                                    <h6 class="h6">CAN SECURITIES LISTED ON OTHER EXCHANGES BE TRADED ON NASD OTC
                                        MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Equities listed on any registered exchange in Nigeria will not be traded on
                                        NASD.
                                    </p>
                                    <h6 class="h6">WHAT HAPPENS WHEN A SECURITY THAT WAS TRADING ON THE OTC MARKET GETS
                                        LISTED ON ANOTHER EXCHANGE?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        By default all securities of public companies not quoted on another exchange
                                        qualify to be traded on the NASD OTC
                                        market. Once a security has been approved for listing on another exchange, that
                                        exchange will inform NASD in writing
                                        that the security in question is about to be listed. Upon receipt of this
                                        written notification, NASD will stop trading
                                        in that security and remove it from the NASD platform.
                                    </p>
                                    <h6 class="h6">WHERE CAN I GET A LIST OF ALL THE TRADED SECURITIES ON NASD OTC
                                        MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        A comprehensive list of all the securities trading on the NASD OTC market is
                                        available on our website under the menu
                                        "Market > Securities Trading" Section. How do I get financial records of
                                        companies that are trading on NASD OTC market?
                                        Where such information is available online, NASD will provide a direct link to
                                        the financial accounts and statements on
                                        the issuer's website.
                                    </p>
                                    <h6 class="h6">HOW ARE PRICES OF SECURITIES DETERMINED ON THE NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Prices are determined by demand and supply. Investors are advised to select
                                        Brokers who can analyse the company's
                                        financials and advise on appropriate bid and offer values for securities of
                                        interest. Where bids exceed offers, the
                                        price of the security may rise and where offers exceed bids, the price may be
                                        expected to fall to a level where a deal
                                        may be struck.
                                    </p>
                                    <h6 class="h6">HOW WILL TRADE ON NASD OTC MARKET BE SETTLED?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        All trades will be settled through Central Securities Clearing System [“CSCS”]
                                        on T+3 basis (that is, Transaction date
                                        plus three days).
                                    </p>
                                    <h6 class="h6">WHERE CAN I GET THE PRICELIST OF SECURITIES TRADING ON NASD OTC
                                        MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        You can get the Pricelist of securities trading on NASD OTC market from our
                                        website under the heading “Daily Market
                                        Summary”. View price list here
                                    </p>
                                    <h6 class="h6">HOW CAN I BUY OR SELL UNLISTED SECURITIES ON NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        To buy or sell shares on NASD OTC market, potential investors should contact one
                                        of our licensed stockbrokers. Our
                                        Stockbroker list has been categorized into two namely Broker/Dealer Firms and
                                        Broker Firms and a comprehensive list is
                                        available here. Stockbrokers will also be happy to provide you with other
                                        available information on unlisted shares.
                                    </p>
                                    <h6 class="h6">WILL PRICE MOVEMENTS BE RESTRICTED TO A DAILY PERCENTAGE ON STOCKS?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Yes. NASD operates a 15% +/- price bands as prices will be determined solely by
                                        the forces of demand and supply.
                                    </p>
                                    <h6 class="h6">HOW CAN INVESTORS MONITOR PRICE MOVEMENTS ON NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Price movements are documented in the market index. Daily movements are also
                                        available on the market ticker on our
                                        website. The Daily Market summary allows the public see volumes traded and the
                                        deal prices.
                                    </p>
                                    <h6 class="h6">DOES NASD ACT AS A CENTRAL COUNTER PARTY?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        No, NASD is an organised market that provides a platform where all unquoted
                                        securities can be traded between sellers and
                                        buyers on behalf of the investors. All trades on the platform are executed by
                                        NASD licensed Participating Institutions
                                    </p>
                                    <h6 class="h6">DOES NASD INVEST DIRECTLY IN INSTRUMENTS TRADED ON ITS PLATFORM?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        No. NASD Plc is a neutral operator in the financial markets. It only provides
                                        platforms for trades to be established.
                                    </p>
                                    <h6 class="h6">IS THERE A FREE FLOAT REQUIREMENT ON THE NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        There is a 10% free float requirement for admitted companies. No such
                                        requirement exists for companies whose securities
                                        are merely admitted to trade.
                                    </p>
                                    <h6 class="h6">HOW DOES NASD REFLECT CORPORATE ACTIONS ON SECURITIES THAT ARE
                                        TRADING ON THE OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        As soon as NASD becomes aware of the information, it will announce the corporate
                                        actions of securities trading on the
                                        OTC Market on the website. As an OTC market, we do not intervene in valuation
                                        and pricing but shall make all information
                                        necessary for appropriate pricing available to the public.
                                    </p>
                                    <h6 class="h6">CAN SECURITIES TRADED ON NASD OTC PLATFORM BE USED AS COLLATERAL?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Yes. Lenders will be pleased to learn that OTC trading stocks qualify for lien
                                        arrangements. NASD provides universally
                                        acceptable prices as a reference while the CSCS will hold dematerialised shares
                                        according to the terms of the borrower /
                                        lender agreement. For a full list of NASD OTC licensed stockbrokers who can
                                        structure your lien arrangement please
                                        navigate to: Market > Participating Institution > Broker/Dealer Firms on our
                                        website.
                                    </p>
                                    <h6 class="h6">HOW CAN I VERIFY MY SHARE CERTIFICATE?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                    <ol>
                                        <li>You will instruct a licensed Stockbroker to verify his signature and open a
                                            depository account at CSCS.
                                        </li>
                                        <li>The stockbroker will attach the certificate with a duly signed transfer form
                                            and send to the Company’s Registrar for
                                            verification.
                                        </li>
                                        <li>The Registrar will verify the signature securing the certificate and confirm
                                            the investors holding. Once verified, the
                                            registrar will send a book entry of the investors holding to the depository
                                            account held in CSCS iv. CSCS will credit
                                            the Investor’s account with units of shares verified and the broker can
                                            offer to sell the amount credited at the start
                                            of the next trading day.
                                        </li>
                                    </ol>
                                    </p>
                                    <h6 class="h6">WHAT IS DEMATERIALIZATION?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        This is a process in which physical share certificates are converted into
                                        electronic format and domiciled with a
                                        registered depository. CSCS is currently the only company that provides
                                        depository services to the Nigerian capital
                                        market. Share certificates must be converted into electronic form before they
                                        can be traded on the NASD OTC market.
                                    </p>
                                    <h6 class="h6">DO I NEED TO OPEN A SEPARATE CSCS ACCOUNT FOR NASD OTC MARKET?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Yes, you need to open a CSCS account for transactions on NASD OTC Market. Simply
                                        contact your Stockbroker and a new
                                        account will be opened on your behalf with CSCS.
                                    </p>
                                    <h6 class="h6">TO WHOM DO I TAKE MY SHARE CERTIFICATES FOR DEMATERIALISATION?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Take your share certificates to a stockbroker who will present them for
                                        verification and subsequent dematerialisation.
                                    </p>
                                    <h6 class="h6">WHY DO I NEED TO SIGN IN TWO PLACES ON THE NASD TRANSFER FORM?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        You need to sign in the two places to cover both verification and
                                        dematerialisation of the certificate into your CSCS
                                        account.
                                    </p>
                                    <h6 class="h6">IF I HAVE NOT RECEIVED MY SHARE CERTIFICATE FROM THE ISSUER, WHAT
                                        SHOULD I DO?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Your Stockbroker should contact the Registrar and obtain your certificate for
                                        you.
                                    </p>
                                    <h6 class="h6">CAN INVESTORS STILL OBTAIN A CERTIFICATE IF SO DESIRED?
                                    </h6>
                                    <p class="mb-2" style="text-align: justify;">
                                        Yes. Although the dematerialisation process is set up to provide greater
                                        security and easy access, Investors may
                                        approach the Registrars and request for a physical certificate. Instruments
                                        Tradable.
                                    </p>
